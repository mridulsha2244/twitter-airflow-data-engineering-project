sudo apt update → Refresh package list from repositories.

sudo apt install python3.12-venv → Install virtual environment support package.

python3 -m venv venv → Create isolated Python development environment.

source venv/bin/activate → Activate virtual environment for package control.

pip install pandas → Install data analysis library Pandas.

pip install s3fs → Enable Python interaction with AWS S3.

pip install tweepy → Use Twitter API with Python.

pip install apache-airflow → Install Airflow for workflow orchestration.

airflow standalone → Start Airflow locally for development/testing.
